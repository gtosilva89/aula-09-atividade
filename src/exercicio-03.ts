// Utilizando (For), construir um programa que apresente todos os valores numéricos divisíveis por 4 e menores que 200.
// Sugestão: a variável que controla o contador do laço deve ser iniciada com valor 1.

for (let i = 1; i <= 200; i++) {
    if (!(i%4))
      console.log("O número " + i + " é divisivel por 4")
}
